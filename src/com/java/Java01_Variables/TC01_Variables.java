package com.java.Java01_Variables;

public class TC01_Variables 
{
	
	//Variables are used to hold some data, value of the variable can be changed
	
	//1.Numeric value Data Type variables
	byte b;   // 1 byte of memory 
	short s;  // 2 byte
	int i;    //4 byte
	long l;  // 8 byte
	
	//Note: byte, short, int , long are used to hold numeric data type values not decimal value
	
	//here in interger we can define decimal values like int j=10.23. It will shows an error
	//to overcome this problem we will use float an double data types
	
   //2.Decimal Value Data Types
	float f=50;
	double d=23.56;
	
   // What is difference between float and double
   // float data  type takes 4 bytes of space
   // double data type takes 8 bytes of space
   // If you want to store small decimal values you can use float data type
   // If you want to store big decimal values you can use double data type
	
	char ch='a';
	boolean b1=true;
	
   //3.Constant Data Type Variables
   //Constants variables are used to hold some data, value of constant can not be changed
	final int k=10;
	final long l1=50;
	
	

}
