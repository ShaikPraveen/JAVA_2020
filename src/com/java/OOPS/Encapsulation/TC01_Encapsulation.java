package com.java.OOPS.Encapsulation;

public class TC01_Encapsulation 
{
	private int id;
	private String name;
	
	
	//Setter Method
	public void setId(int id)
	{
		this.id=id;
	}
	
	//Getter Method
	public int getId()
	{
		return id;
	}
	
	public void setname(String name)
	{
		this.name=name;
	}
	
	public String getname()
	{
		return name;
	}

}
